public class Aula008 {
    public static void main(String[] args) {
        
        /**OPERADORES LÓGICOS
         * && E
         * || OU
         * ! NOT
        */
        
        int  x = 6;
        
        System.out.println((x > 5) && (x < 7)); // E
        System.out.println((x > 5) || (x < 7)); // OU
        System.out.println(!(x > 5)); // Negação
    }
}
